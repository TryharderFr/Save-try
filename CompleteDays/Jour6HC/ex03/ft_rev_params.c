void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int i;
	int j;
	int n;

	i = argc - 1;
	n = 1;
	while (i > 0)
	{
		j = 0;
		while (argv[i][j])
		{
			ft_putchar(argv[i][j]);
			j++;
		}
		ft_putchar('\n');
		i--;
		n++;
	}
	return (0);
}
